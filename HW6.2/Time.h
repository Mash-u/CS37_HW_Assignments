#ifndef CS37HWASSIGNMENTS_TIME_H
#define CS37HWASSIGNMENTS_TIME_H

class Time
{
public:
	/************************************************************************************************************************************************************
	* NAME         : Time()
	* DESCRIPTION  : Default constructor for the Time class. Takes no formal paramters and sets instance variables to 0.
	* RETURN VALUE : no return value
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------
	*************************************************************************************************************************************************************/
	Time();
	/************************************************************************************************************************************************************
	* NAME         : Time(int h, int m, int s)
	* DESCRIPTION  : Constructor that has 3 formal parameters. Will set instance variables to the parameter values.
	* RETURN VALUE : no return value.
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	Time(int newH, int newM, int newS);

	/************************************************************************************************************************************************************
	* NAME         : setTime(int h, int m, int s)
	* DESCRIPTION  : setMEthod that takes 3 int values as formal parameters. Will set the Time objects instance variables to the parameter values.
	* RETURN VALUE : void
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	void setTime(int h, int m, int s);

	/************************************************************************************************************************************************************
	* NAME         : getHour()
	* DESCRIPTION  : get method that returns the value assigned to instance variable member hour.
	* RETURN VALUE : int
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	int getHour();

	/************************************************************************************************************************************************************
	* NAME         : getMin()
	* DESCRIPTION  : get method that returns the value assigned to instance variable member min.
	* RETURN VALUE : int
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	int getMin();

	/************************************************************************************************************************************************************
	* NAME         : getSec()
	* DESCRIPTION  : get method that returns the value assigned to instance variable member min.
	* RETURN VALUE : int
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	int getSec();

	/************************************************************************************************************************************************************
	* NAME         : compare(int h, int m, int s)
	* DESCRIPTION  : Method that takes 3 int variables as its formal parameter and returns a bool value depending on whether theyre equal to the TIme objects 
	*                instance variables.
	* RETURN VALUE : bool
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	bool compare(int, int, int);

	/************************************************************************************************************************************************************
	* NAME         : getTime(int &h, int &m, int &s)
	* DESCRIPTION  : getMethod that passes  3 int variables by reference, and assigns them the values of the instance variables of the Time object.
	* RETURN VALUE : void
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	void getTime(int&, int&, int&);

	/************************************************************************************************************************************************************
	* NAME         : displayMilFormat()
	* DESCRIPTION  : Method that outputs the instance variables in military format on screen.
	* RETURN VALUE : void
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	void displayMilitaryFormat();

	/************************************************************************************************************************************************************
	* NAME         : displayRegularFormat()
	* DESCRIPTION  : Method that uses several selection statements to output the appropriate time in regular format.
	* RETURN VALUE : no return value
	* -----------------------------------------------------------------------------------------------------------------------------------------------------------*
	*************************************************************************************************************************************************************/
	void displayRegularFormat();

private:
	int hour, min, sec;
};

#endif //CS37HWASSIGNMENTS_TIME_H

